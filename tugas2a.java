package com.mycompany.tugas2a; 

public class Tugas2a {
    public static void main(String[] args) {
        
        int a = -8;
        int b = -4;
        int c = -2;

        System.out.println("Hasil a + b + c  = " + (a + b + c));
        System.out.println("Hasil a - b - c  = " + (a - b - c));
        System.out.println("Hasil a * b * c  = " + (a * b * c));
        System.out.println("Hasil a / b / c  = " + (a / b / c));  
    }
}
